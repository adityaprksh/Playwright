
// Exponentiation Operator
// a ** b -> a to the power of b
let power = 2 ** 3; // 2 * 2 * 2
console.log(power);

// find a factorial of a number using exponentiation
let num = 5;
let factorial = 1; 
for(let i = 1; i <= num; i++) {
  factorial *= i; // factorial = factorial * i
}   
console.log(factorial);
