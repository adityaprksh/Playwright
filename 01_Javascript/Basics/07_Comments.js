// Examples of comments in JavaScript

// Single-line comments start with two slashes.
// Use them to explain code, mark TODOs, or disable a line temporarily.
let greeting = 'Hello, world!';
console.log(greeting); // This prints the greeting to the console

/*
  Multi-line comments start with /* and end with 
  They are useful for longer explanations or disabling blocks of code.
*/

function add(a, b) {
  return a + b;
}

console.log(add(2, 3));

/*
  Commenting best practices:
  - Keep comments concise and accurate.
  - Explain why code exists, not just what it does.
  - Remove outdated comments when code changes.
*/
